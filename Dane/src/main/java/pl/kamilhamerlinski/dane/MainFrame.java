    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            FileWriter writer = new FileWriter("zapis.txt", true);
            if(!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("") && !jTextField4.getText().equals("")){
                writer.write(jTextField1.getText() + ";" + jTextField2.getText() + ";" + jTextField3.getText() + ";" + jTextField4.getText() + "\n");
                writer.close();
            }
        } catch (IOException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void loadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadButtonActionPerformed
        try {
            Scanner sc = new Scanner(new File("zapis.txt"));
            String out = "";
            while(sc.hasNext()){
                String[] raw = sc.nextLine().split(";");
                for(int x = 0; x < raw.length; x++){
                    out += raw[x] + " ";
                }
                out += "\n";
            }
            jTextArea1.setText(out);
            sc.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loadButtonActionPerformed

