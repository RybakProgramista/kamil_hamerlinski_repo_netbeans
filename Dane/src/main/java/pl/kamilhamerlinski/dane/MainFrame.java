    private void jTextField3FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField3FocusLost
        char[] c = jTextField3.getText().toCharArray();
        boolean isMonkey = false, isCharAfterMonkey = false, isDot = false, isCharAfterDot = false;
        for(int x  = 0; x < c.length; x++){
            if(c[x] == "@".charAt(0) && !isMonkey){
                isMonkey = true;
            }
            if(c[x] != ".".charAt(0) && isMonkey && !isDot && !isCharAfterDot){
                isCharAfterMonkey = true;
            }
            if(c[x] == ".".charAt(0) && isMonkey && isDot){
                isDot = true;
            }
            if(isMonkey && isCharAfterMonkey && isDot){
                isCharAfterDot = true;
            }
        }
        if(!isMonkey && !isCharAfterMonkey && !isDot && !isCharAfterDot){
            jTextField3.setText("");
        }
    }//GEN-LAST:event_jTextField3FocusLost

